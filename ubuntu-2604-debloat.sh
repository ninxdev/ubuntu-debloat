Universal Ubuntu 26.04 LTS GNOME 50 Debloat Script

OPTIONS:
    --yes              Non-interactive mode (auto-yes to all prompts)
    --dry-run          Show what would be done without executing
    --verbose          Verbose output
    --restore          Run rollback script from last run
    --help             Show this help
    --version          Show version

EXAMPLES:
    sudo $0                    # Interactive mode
    sudo $0 --yes              # Fully automated
    sudo $0 --dry-run --verbose # Preview changes
    sudo $0 --restore          # Rollback last run

REQUIREMENTS:
    - Ubuntu 26.04 LTS (resolute)
    - GNOME 50+
    - Linux kernel 7.x
    - Root privileges

WHAT THIS SCRIPT DOES:
  1. Removes snap completely and pins to prevent reinstall
  2. Removes flatpak and pins to prevent reinstall
  3. Removes Ubuntu GNOME extensions and pins
  4. Installs GNOME Extension Manager (native apt)
  4. Removes GNOME bloat apps (calculator, weather, maps, games, etc.)
  5. Hides unremovable apps (Help, Language Support, Network Config)
  6. Removes Ubuntu bloat (metapackages, telemetry, software centers, etc.)
  8. Pins ALL bloat packages to prevent reinstall on updates
  9. Holds packages via apt-mark hold

WHAT THIS SCRIPT DOES NOT DO:
  ✗ Install any IDE (VS Code, JetBrains, etc.)
  ✗ Install any browser (Chrome, Firefox, etc.)
  ✗ Install any terminal (Ghostty, Kitty, etc.)
  ✗ Install any user-specific tools
  ✗ Modify user configs or dotfiles

ROLLBACK:
  Run 'sudo /tmp/ubuntu-debloat-rollback-<timestamp>/rollback.sh'
  or use --restore flag (if implemented)

EOF
}

# ============================================================================
# MAIN
# ============================================================================
parse_args() {
    while [[ $# -gt 0 ]]; do
        case $1 in
            --yes) AUTO_YES=true ;;
            --dry-run) DRY_RUN=true ;;
            --verbose) VERBOSE=true ;;
            --restore)
                local latest_rollback
                latest_rollback=$(ls -dt /tmp/ubuntu-debloat-rollback-* 2>/dev/null | head -1)
                [[ -n "$latest_rollback" ]] || die "No rollback found"
                bash "$latest_rollback/rollback.sh"
                exit 0
                ;;
            --help) usage; exit 0 ;;
            --version) echo "$SCRIPT_NAME v$VERSION"; exit 0 ;;
            *) die "Unknown option: $1. Use --help." ;;
        esac
        shift
    done
}

main() {
    parse_args "$@"

    # Pre-flight
    check_root
    check_ubuntu_version
    check_gnome_version
    check_kernel_version

    setup_rollback

    info "Starting $SCRIPT_NAME v$VERSION"
    info "Target: Ubuntu 26.04 LTS, GNOME 50, Kernel 7.x"
    [[ "$DRY_RUN" == true ]] && warn "DRY-RUN MODE - No changes will be made"

    apt_update

    # Execute phases
    remove_snap
    pin_snap

    remove_flatpak
    pin_flatpak

    remove_ubuntu_extensions
    pin_ubuntu_extensions

    install_extension_manager

    remove_gnome_bloat
    hide_unremovable_apps

    remove_ubuntu_bloat
    pin_all_bloat

    cleanup
    verify

    info "Debloat complete! Rollback available at: ${ROLLBACK_DIR}/rollback.sh"
    info "Log file: ${LOG_FILE}"
}

main "$@"
