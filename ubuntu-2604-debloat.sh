#!/bin/bash
# ubuntu-2604-debloat.sh
# Universal Ubuntu 26.04 LTS GNOME 50 Debloat Script
# Removes snap, flatpak, Ubuntu bloat, GNOME bloat; pins to prevent reinstall
# Installs GNOME Extension Manager; hides unremovable apps
# No user-specific apps (IDE, browser, terminal) installed
# Tested on Ubuntu 26.04 LTS (resolute) with GNOME 50, kernel 7.x
# Run as root: sudo ./ubuntu-2604-debloat.sh [--yes] [--dry-run] [--verbose]

set -euo pipefail

# ============================================================================
# CONFIGURATION & GLOBALS
# ============================================================================
SCRIPT_NAME="ubuntu-2604-debloat"
VERSION="1.0.0"
DRY_RUN=false
VERBOSE=false
AUTO_YES=false
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
ROLLBACK_DIR="/tmp/ubuntu-debloat-rollback-${TIMESTAMP}"
LOG_FILE="${ROLLBACK_DIR}/debloat.log"
APT_PREFS_BACKUP="${ROLLBACK_DIR}/apt-prefs-backup"
INSTALLED_PKGS_FILE="${ROLLBACK_DIR}/installed-packages.txt"
APT_PREFS_DIR="/etc/apt/preferences.d"
BACKUP_APT_PREFS_DIR="${ROLLBACK_DIR}/apt-prefs.d.backup"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================
log() { echo -e "${BLUE}[$(date '+%H:%M:%S')]${NC} $*" | tee -a "$LOG_FILE"; }
info() { echo -e "${GREEN}[INFO]${NC} $*" | tee -a "$LOG_FILE"; }
warn() { echo -e "${YELLOW}[WARN]${NC} $*" | tee -a "$LOG_FILE"; }
error() { echo -e "${RED}[ERROR]${NC} $*" | tee -a "$LOG_FILE"; }
die() { error "$*"; exit 1; }

run() {
    if [[ "$DRY_RUN" == true ]]; then
        info "[DRY-RUN] $*"
    else
        eval "$@" 2>&1 | tee -a "$LOG_FILE"
    fi
}

apt_install() { run "apt install -y $*"; }
apt_remove() { run "apt remove --purge -y $*"; }
apt_update() { run "apt update"; }

is_installed() { dpkg -l | grep -q "^ii  $1 "; }

# ============================================================================
# PRE-FLIGHT CHECKS
# ============================================================================
check_root() {
    [[ $EUID -eq 0 ]] || die "Must run as root. Use: sudo $0"
}

check_ubuntu_version() {
    local version
    version=$(lsb_release -rs 2>/dev/null || echo "unknown")
    [[ "$version" == "26.04" ]] || die "This script requires Ubuntu 26.04 LTS. Found: $version"
}

check_gnome_version() {
    local version
    version=$(gnome-shell --version 2>/dev/null | grep -oE '[0-9]+' | head -1 || echo "0")
    [[ "$version" -ge 50 ]] || die "This script requires GNOME 50+. Found: $version"
}

check_kernel_version() {
    local major
    major=$(uname -r | cut -d. -f1)
    [[ "$major" -ge 7 ]] || warn "Kernel major version < 7 (found: $major). Proceeding anyway."
}

# ============================================================================
# BACKUP & ROLLBACK SETUP
# ============================================================================
setup_rollback() {
    mkdir -p "$ROLLBACK_DIR" "$BACKUP_APT_PREFS_DIR"
    touch "$LOG_FILE"
    chmod 644 "$LOG_FILE"

    # Backup apt preferences
    [[ -d "$APT_PREFS_DIR" ]] && cp -r "$APT_PREFS_DIR" "$BACKUP_APT_PREFS_DIR"

    # Record installed packages
    dpkg -l | grep '^ii' > "$INSTALLED_PKGS_FILE"

    # Create rollback script
    cat > "${ROLLBACK_DIR}/rollback.sh" << 'ROLLBACK_EOF'
#!/bin/bash
# Rollback script generated by ubuntu-2604-debloat.sh
set -euo pipefail

ROLLBACK_DIR="$(dirname "$0")"
APT_PREFS_DIR="/etc/apt/preferences.d"
BACKUP_DIR="${ROLLBACK_DIR}/apt-prefs.d.backup"
LOG_FILE="${ROLLBACK_DIR}/debloat.log"

echo "[ROLLBACK] Restoring apt preferences..."
rm -rf "$APT_PREFS_DIR"
cp -r "$BACKUP_DIR" "$APT_PREFS_DIR"

echo "[ROLLBACK] Restoring packages from backup..."
# Note: This only restores apt config. Package reinstallation must be manual.
echo "[ROLLBACK] Apt preferences restored. Run 'apt update' and reinstall packages manually if needed."
ROLLBACK_EOF
    chmod +x "${ROLLBACK_DIR}/rollback.sh"

    info "Rollback script created at ${ROLLBACK_DIR}/rollback.sh"
    info "Backup stored in ${ROLLBACK_DIR}"
}

# ============================================================================
# PHASE 1: SNAP REMOVAL & PINNING
# ============================================================================
remove_snap() {
    info "Phase: Removing Snap..."

    # Stop and disable snap services
    run "systemctl stop snapd.service snapd.socket snapd.apparmor.service 2>/dev/null || true"
    run "systemctl disable snapd.service snapd.socket snapd.apparmor.service 2>/dev/null || true"

    # Remove all snaps
    if command -v snap &>/dev/null; then
        snap list --all 2>/dev/null | awk '/disabled/{print $1, $3}' | while read -r snapname revision; do
            [[ -n "$snapname" && -n "$revision" ]] && run "snap remove \"$snapname\" --revision=\"$revision\""
        done
    fi

    # Remove snapd package
    if is_installed snapd; then
        apt_remove snapd
    fi

    # Clean snap directories
    run "rm -rf /snap /var/snap /var/lib/snapd /var/cache/snapd /home/*/snap 2>/dev/null || true"
    info "Snap removed and cleaned."
}

pin_snap() {
    info "Phase: Pinning Snap to prevent reinstall..."
    cat > /etc/apt/preferences.d/99-block-snap << 'EOF'
# Block Snap permanently
Package: snapd
Pin: release *
Pin-Priority: -1

Package: snap-confine
Pin: release *
Pin-Priority: -1

Package: snapd-glib
Pin: release *
Pin-Priority: -1

Package: gir1.2-snapd-2
Pin: release *
Pin-Priority: -1

Package: libsnapd-glib*
Pin: release *
Pin-Priority: -1
EOF
    info "Snap pinned (Pin-Priority: -1)."
}

# ============================================================================
# PHASE 2: FLATPAK REMOVAL & PINNING
# ============================================================================
remove_flatpak() {
    info "Phase: Removing Flatpak..."
    if is_installed flatpak; then
        apt_remove flatpak
    fi
    run "rm -rf /var/lib/flatpak /home/*/.local/share/flatpak /var/lib/flatpak 2>/dev/null || true"
    info "Flatpak removed."
}

