# ============================================================================
# PHASE 9: CLEANUP
# ============================================================================
cleanup() {
    info "Phase: Cleaning up..."
    run "apt autoremove --purge -y"
    run "apt clean"
    run "apt autoclean"
    info "Cleanup complete."
}

# ============================================================================
# VERIFICATION
# ============================================================================
verify() {
    info "Phase: Verifying installation..."

    local failed=0

    # Check snap removed
    if command -v snap &>/dev/null; then
        warn "snap command still exists"
        ((failed++))
    else
        info "✓ snap removed"
    fi

    # Check flatpak removed
    if command -v flatpak &>/dev/null; then
        warn "flatpak command still exists"
        ((failed++))
    else
        info "✓ flatpak removed"
    fi

    # Check Ubuntu extensions removed
    if is_installed gnome-shell-ubuntu-extensions; then
        warn "gnome-shell-ubuntu-extensions still installed"
        ((failed++))
    else
        info "✓ Ubuntu GNOME Extensions removed"
    fi

    # Check Extension Manager installed
    if is_installed gnome-shell-extension-manager; then
        info "✓ GNOME Extension Manager installed"
    else
        warn "gnome-shell-extension-manager not installed"
        ((failed++))
    fi

    # Check pinned packages
    local pinned_count
    pinned_count=$(grep -r "Pin-Priority: -1" /etc/apt/preferences.d/ 2>/dev/null | wc -l)
    info "✓ $pinned_count packages pinned"

    # Check held packages
    local held_count
    held_count=$(apt-mark showhold 2>/dev/null | wc -l)
    info "✓ $held_count packages held"

    if [[ $failed -eq 0 ]]; then
        info "All verifications passed!"
    else
        warn "$failed verification(s) failed"
    fi
}

