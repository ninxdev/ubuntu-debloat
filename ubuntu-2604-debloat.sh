Pin-Priority: -1

Package: libflatpak*
Pin: release *
Pin-Priority: -1

Package: libostree*
Pin: release *
Pin-Priority: -1
EOF
    info "Flatpak pinned (Pin-Priority: -1)."
}

# ============================================================================
# PHASE 3: UBUNTU GNOME EXTENSIONS REMOVAL & PINNING
# ============================================================================
remove_ubuntu_extensions() {
    info "Phase: Removing Ubuntu GNOME Extensions..."
    if is_installed gnome-shell-ubuntu-extensions; then
        apt_remove gnome-shell-ubuntu-extensions
    else
        info "gnome-shell-ubuntu-extensions not installed."
    fi
}

pin_ubuntu_extensions() {
    info "Phase: Pinning Ubuntu GNOME Extensions..."
    cat > /etc/apt/preferences.d/99-block-ubuntu-extensions << 'EOF'
# Block Ubuntu GNOME Extensions permanently
Package: gnome-shell-ubuntu-extensions
Pin: release *
Pin-Priority: -1
EOF
    info "Ubuntu GNOME Extensions pinned."
}

# ============================================================================
# PHASE 4: INSTALL GNOME EXTENSION MANAGER
# ============================================================================
install_extension_manager() {
    info "Phase: Installing GNOME Extension Manager..."
    apt_update
    if is_installed gnome-shell-extension-manager; then
        info "gnome-shell-extension-manager already installed."
    else
        apt_install gnome-shell-extension-manager
    fi
}

# ============================================================================
# PHASE 5: REMOVE GNOME BLOAT APPS
# ============================================================================
remove_gnome_bloat() {
    info "Phase: Removing GNOME bloat apps..."

    # Apps that can be safely removed (no critical deps)
    local bloat_packages=(
        # Core GNOME apps
        gnome-calculator gnome-characters gnome-clocks gnome-logs
        gnome-weather gnome-maps gnome-music gnome gnome-photos
        gnome-contacts gnome-calendar gnome-font-viewer
        gnome-text-editor gnome-system-monitor gnome-disk-utility
        gnome-terminal gnome-console gnome-font-viewer gnome-text-editor
        
        # Media
        totem rhythmbox cheese simple-scan
        
        # Utilities
        baobab seahorse
        
        # Games
        gnome-mahjongg gnome-mines gnome-sudoku gnome-taquin gnome-nibbles
        gnome-robots gnome-tetravex five-or-more four-in-a-row hitori
        lightsoff quadrapassel swell-foop tali
        
        # Developer tools (if present)
        gnome-builder gnome-code-assistance
    )

    for pkg in "${bloat_packages[@]}"; do
        if is_installed "$pkg"; then
            apt_remove "$pkg"
        else
            [[ "$VERBOSE" == true ]] && info "$pkg not installed, skipping."
        fi
    done

    # Remove orphaned dependencies
    run "apt autoremove --purge -y"
    info "GNOME bloat apps removed."
}

# ============================================================================
# PHASE 6: HIDE UNREMOVABLE GNOME APPS
# ============================================================================
hide_unremovable_apps() {
    info "Phase: Hiding unremovable GNOME apps..."

    # Apps that can't be removed (dependencies) but should be hidden
    local hide_apps=(
        "org.gnome.Yelp.desktop"
        "gnome-language-selector.desktop"
        "nm-connection-editor.desktop"
    )

    # System-wide hide (requires root)
    for desktop in "${hide_apps[@]}"; do
        local src="/usr/share/applications/$desktop"
        if [[ -f "$src" ]]; then
            cp "$src" "${src}.bak"
            sed -i '/^\[Desktop Entry\]/a NoDisplay=true' "$src"
            info "Hidden (system): $desktop"
        fi
    done

    # User-specific hide (for current user)
    local user_home="${SUDO_HOME:-$HOME}"
    local user_apps_dir="$user_home/.local/share/applications"
    mkdir -p "$user_apps_dir"

    for desktop in "${hide_apps[@]}"; do
        local src="/usr/share/applications/$desktop"
        if [[ -f "$src" ]]; then
            cp "$src" "$user_apps_dir/"
            sed -i '/^\[Desktop Entry\]/a NoDisplay=true' "$user_apps_dir/$desktop"
            info "Hidden (user): $desktop"
        fi
    done

    info "Unremovable apps hidden via NoDisplay=true."
}

